type AppTranslations = typeof appTranslations.en;
type DeepWiden<T> =
  T extends string ? string :
  T extends readonly (infer U)[] ? readonly DeepWiden<U>[] :
  T extends object ? { [K in keyof T]: DeepWiden<T[K]> } :
  T;

const i18n: Record<Language, DeepWiden<AppTranslations>> = appTranslations;

export function useT(language: Language) {
  return i18n[language];
}
